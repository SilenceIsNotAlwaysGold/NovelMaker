// Anti-AI Checklist
// ============================================================

export const antiAIRules = `【去AI味规则（严格遵守）】
黑名单词汇（绝对不用）：
璀璨、瑰丽、绚烂、宛如、犹如、美轮美奂、此外、然而、尽管如此、
值得注意的是、不可否认、深邃眼眸、如墨长发、精致五官、
内心深处、心中涌起、复杂的情感、不禁、仿佛、恍若

替换策略：
1. 抽象→具体动作："他很愤怒"→"他一拳砸在桌上，茶杯弹起"
2. 书面→口语："应当立即采取行动"→"赶紧走"
3. 心理描写→外化行为："内心矛盾"→"他盯着照片看了很久，最终翻面扣下"
4. 长句→长短交错，打散节奏单调
5. 连续四字成语→每段最多1个

自查要求：
- "感到/觉得"每章≤3次
- 不能连续3段用"他/她"开头
- 对话必须像人话（朗读检验）
- 句长要有变化（不连续3句相同长度）`

