export const expansionTechniques = `【扩写技巧（字数不足时使用）】
1. 场景感官化（+300-500字）：选2-3种感官，用具体细节代替抽象
2. 动作分解（+200-400字）：关键动作慢镜头分解（仅用于高潮/关键操作）
3. 对话加深（+300-600字）：加潜台词、动作beats、打断
4. 内心外化（+200-400字）：不写"他很紧张"→写"他在撕矿泉水标签"
5. 次要互动（+200-400字）：赶路闲聊中埋信息、吃饭细节展示关系
6. 环境压力（+200-300字）：天黑、下雨、温度、远处脚步声制造紧迫

扩充禁忌：大段无用风景 / 重复已知信息 / 无冲突流水账 / 强行新角色 / 过长独白`

